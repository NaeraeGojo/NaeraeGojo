package kr.or.ddit.vo;

public class VideoChatVO {
	
	// 화상회의
	
	private String video_chat_code;
	private String video_chat_maker;
	private String video_chat_date;
	private String video_chat_rp;
	private String video_chat_delete;

	public String getVideo_chat_code() {
		return video_chat_code;
	}
	public void setVideo_chat_code(String video_chat_code) {
		this.video_chat_code = video_chat_code;
	}
	public String getVideo_chat_maker() {
		return video_chat_maker;
	}
	public void setVideo_chat_maker(String video_chat_maker) {
		this.video_chat_maker = video_chat_maker;
	}
	public String getVideo_chat_date() {
		return video_chat_date;
	}
	public void setVideo_chat_date(String video_chat_date) {
		this.video_chat_date = video_chat_date;
	}
	public String getVideo_chat_rp() {
		return video_chat_rp;
	}
	public void setVideo_chat_rp(String video_chat_rp) {
		this.video_chat_rp = video_chat_rp;
	}
	public String getVideo_chat_delete() {
		return video_chat_delete;
	}
	public void setVideo_chat_delete(String video_chat_delete) {
		this.video_chat_delete = video_chat_delete;
	}
}
