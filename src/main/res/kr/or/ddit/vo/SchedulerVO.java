package kr.or.ddit.vo;

public class SchedulerVO {

	// 스케줄러
	
	private String scheduler_code;
	private String scheduler_time;
	private String scheduler_title;
	private String scheduler_explain;
	private String scheduler_writer;
	private String scheduler_keyword;

	public String getScheduler_code() {
		return scheduler_code;
	}
	public void setScheduler_code(String scheduler_code) {
		this.scheduler_code = scheduler_code;
	}
	public String getScheduler_time() {
		return scheduler_time;
	}
	public void setScheduler_time(String scheduler_time) {
		this.scheduler_time = scheduler_time;
	}
	public String getScheduler_title() {
		return scheduler_title;
	}
	public void setScheduler_title(String scheduler_title) {
		this.scheduler_title = scheduler_title;
	}
	public String getScheduler_explain() {
		return scheduler_explain;
	}
	public void setScheduler_explain(String scheduler_explain) {
		this.scheduler_explain = scheduler_explain;
	}
	public String getScheduler_writer() {
		return scheduler_writer;
	}
	public void setScheduler_writer(String scheduler_writer) {
		this.scheduler_writer = scheduler_writer;
	}
	public String getScheduler_keyword() {
		return scheduler_keyword;
	}
	public void setScheduler_keyword(String scheduler_keyword) {
		this.scheduler_keyword = scheduler_keyword;
	}
}
