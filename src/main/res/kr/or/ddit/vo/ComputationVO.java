package kr.or.ddit.vo;

public class ComputationVO {

	// 산출 문서 관리
	
	private String computation_code;
	private String computation_level;
	private String computation_content;
	private String computation_submit;
	private String computation_delete;
	
	public String getComputation_code() {
		return computation_code;
	}
	public void setComputation_code(String computation_code) {
		this.computation_code = computation_code;
	}
	public String getComputation_level() {
		return computation_level;
	}
	public void setComputation_level(String computation_level) {
		this.computation_level = computation_level;
	}
	public String getComputation_content() {
		return computation_content;
	}
	public void setComputation_content(String computation_content) {
		this.computation_content = computation_content;
	}
	public String getComputation_submit() {
		return computation_submit;
	}
	public void setComputation_submit(String computation_submit) {
		this.computation_submit = computation_submit;
	}
	public String getComputation_delete() {
		return computation_delete;
	}
	public void setComputation_delete(String computation_delete) {
		this.computation_delete = computation_delete;
	}
	
	

}
