package kr.or.ddit.vo;

public class PublicInstitutionVO {

	// 공고기관
	
	private String public_institution_code;
	private String public_institution_name;
	private String public_institution_date;
	private String public_institution_delete;

	public String getPublic_institution_code() {
		return public_institution_code;
	}
	public void setPublic_institution_code(String public_institution_code) {
		this.public_institution_code = public_institution_code;
	}
	public String getPublic_institution_name() {
		return public_institution_name;
	}
	public void setPublic_institution_name(String public_institution_name) {
		this.public_institution_name = public_institution_name;
	}
	public String getPublic_institution_date() {
		return public_institution_date;
	}
	public void setPublic_institution_date(String public_institution_date) {
		this.public_institution_date = public_institution_date;
	}
	public String getPublic_institution_delete() {
		return public_institution_delete;
	}
	public void setPublic_institution_delete(String public_institution_delete) {
		this.public_institution_delete = public_institution_delete;
	}
}
