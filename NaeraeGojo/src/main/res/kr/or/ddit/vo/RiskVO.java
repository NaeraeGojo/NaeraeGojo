package kr.or.ddit.vo;

public class RiskVO {

	// 리스크
	
	private String risk_code;
	private String risk_past_work;
	private String risk_event_project;
	private String risk_result;
	private String risk_coment;
	private String risk_delete;

	public String getRisk_code() {
		return risk_code;
	}
	public void setRisk_code(String risk_code) {
		this.risk_code = risk_code;
	}
	public String getRisk_past_work() {
		return risk_past_work;
	}
	public void setRisk_past_work(String risk_past_work) {
		this.risk_past_work = risk_past_work;
	}
	public String getRisk_event_project() {
		return risk_event_project;
	}
	public void setRisk_event_project(String risk_event_project) {
		this.risk_event_project = risk_event_project;
	}
	public String getRisk_result() {
		return risk_result;
	}
	public void setRisk_result(String risk_result) {
		this.risk_result = risk_result;
	}
	public String getRisk_coment() {
		return risk_coment;
	}
	public void setRisk_coment(String risk_coment) {
		this.risk_coment = risk_coment;
	}
	public String getRisk_delete() {
		return risk_delete;
	}
	public void setRisk_delete(String risk_delete) {
		this.risk_delete = risk_delete;
	}
}
